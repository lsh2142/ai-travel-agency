// 도시 정규화 (숙박/액티비티 패턴 동일)
function normalizeCity(destination: string): string | null {
  const d = destination.toLowerCase()
  if (d.includes('오키나와') || d.includes('나하') || d.includes('oka') || d.includes('okinawa')) return 'okinawa'
  if (d.includes('도쿄') || d.includes('tokyo') || d.includes('nrt') || d.includes('hnd')) return 'tokyo'
  if (d.includes('오사카') || d.includes('osaka') || d.includes('kix')) return 'osaka'
  if (d.includes('교토') || d.includes('kyoto')) return 'kyoto'
  if (d.includes('후쿠오카') || d.includes('fukuoka') || d.includes('fuk')) return 'fukuoka'
  if (d.includes('삿포로') || d.includes('sapporo') || d.includes('cts')) return 'sapporo'
  if (d.includes('방콕') || d.includes('bangkok') || d.includes('bkk')) return 'bangkok'
  if (d.includes('싱가포르') || d.includes('singapore') || d.includes('sin')) return 'singapore'
  if (d.includes('발리') || d.includes('bali') || d.includes('dps')) return 'bali'
  if (d.includes('제주') || d.includes('jeju') || d.includes('cju')) return 'jeju'
  return null
}

