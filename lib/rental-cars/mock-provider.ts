function calcDays(from: string, to: string): number {
  const diff = new Date(to).getTime() - new Date(from).getTime()
  const days = Math.max(1, Math.round(diff / (1000 * 60 * 60 * 24)))
  return days
}

export function getMockRentalCars(params: RentalCarSearchParams): RentalCarResult[] {
  const city = normalizeCity(params.destination)
  const baseCars = city ? (BASE_CARS[city] ?? buildGenericSet(params.destination)) : buildGenericSet(params.destination)
  const days = calcDays(params.pickupDate, params.returnDate)

  return baseCars.map((car) => ({
    ...car,
    totalPrice: car.pricePerDay * days,
  }))
}
