// 노선별 mock 데이터 맵
// TYO는 NRT 또는 HND를 가리킴 (도쿄 → NRT 우선)
// OSA는 KIX
export const MOCK_FLIGHTS_BY_ROUTE: Record<string, FlightOption[]> = {
  'ICN-NRT': ICN_NRT,
  'ICN-HND': ICN_HND,
  'ICN-TYO': ICN_NRT,  // 도쿄 일반 → NRT
  'ICN-KIX': ICN_KIX,
  'ICN-OSA': ICN_KIX,  // 오사카 → KIX
  'ICN-BKK': ICN_BKK,
  'ICN-SIN': ICN_SIN,
}

/**
 * 노선 mock 데이터를 date와 함께 반환.
 * date 파라미터로 departure/arrival date 필드를 채워줌.
 */
export function getMockFlights(from: string, to: string, date: string): FlightOption[] {
  const key = `${from}-${to}`
  const base = MOCK_FLIGHTS_BY_ROUTE[key]
  if (!base) return []
  return base.map((f) => ({
    ...f,
    departure: { ...f.departure, date },
    arrival: { ...f.arrival, date },
    bookingUrl: buildGoogleFlightsUrl(f.departure.airport, f.arrival.airport, date),
  }))
}

function buildGoogleFlightsUrl(from: string, to: string, date: string): string {
  return `https://www.google.com/flights#flt=${from}.${to}.${date};c:KRW`
}
